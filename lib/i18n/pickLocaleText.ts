export function pickLocaleText(locale: string, fr: string, en: string): string {
  if (locale === "fr") return fr;
  const everyWeeks = /^Every (\d+) week(s)?$/i.exec(en);
  if (locale === "de") {
    if (everyWeeks) {
      const n = everyWeeks[1];
      return `Alle ${n} Woche${n === "1" ? "" : "n"}`;
    }
    return DE_BY_EN[en] ?? en;
  }
  if (locale === "it") {
    if (everyWeeks) {
      const n = everyWeeks[1];
      return `Ogni ${n} settimana${n === "1" ? "" : "e"}`;
    }
    return IT_BY_EN[en] ?? en;
  }
  return en;
}
